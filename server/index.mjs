function createDownloadToken(orderId, filePath) {
  const token = crypto.randomBytes(24).toString('hex');
  const expiresAt = Date.now() + DOWNLOAD_TTL_MS;
  run('INSERT INTO downloads (id, order_id, token, file_path, expires_at, created_at) VALUES (?, ?, ?, ?, ?, ?)', [
    `dl_${Date.now()}_${Math.random().toString(16).slice(2)}`,
    orderId,
    token,
    filePath,
    expiresAt,
    Date.now()
  ]);
  return token;
}

function getOrCreateDownloadToken(orderId, filePath) {
  const existing = get('SELECT token, expires_at FROM downloads WHERE order_id = ? ORDER BY created_at DESC LIMIT 1', [orderId]);
  if (existing && Date.now() < Number(existing.expires_at)) return existing.token;
  return createDownloadToken(orderId, filePath);
}

