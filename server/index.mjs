async function processStripeSession(session) {
  if (!session || session.payment_status !== 'paid') return null;
  const productId = session.metadata?.productId;
  const product = productId ? get('SELECT * FROM products WHERE id = ?', [productId]) : null;
  if (!product) return null;

  const existing = findOrderByProviderRef(session.id);
  const orderId = existing?.id || recordSale({
    provider: 'stripe',
    amount: Number(product.price),
    currency: String(product.currency),
    productId: product.id,
    buyerEmail: session.customer_details?.email || 'unknown',
    providerRef: session.id
  });

  const token = getOrCreateDownloadToken(orderId, product.file_path);
  const downloadUrl = `${API_BASE_URL}/api/download/${token}`;
  await deliverIfPossible(orderId, session.customer_details?.email || 'unknown', product.title, downloadUrl);
  return { orderId, downloadUrl };
}

async function processWebhookJobs() {
  const jobs = all('SELECT * FROM webhook_jobs WHERE status = ? AND next_run <= ? ORDER BY created_at ASC LIMIT 5', [
    'pending',
    Date.now()
  ]);

  for (const job of jobs) {
    try {
      const payload = JSON.parse(job.payload);
      if (job.provider === 'stripe') {
        if (job.event_type === 'checkout.session.completed' || job.event_type === 'checkout.session.async_payment_succeeded') {
          await processStripeSession(payload.data?.object || payload.data || payload);
        }
      }
      markJobDone(job.id);
    } catch (err) {
      const nextAttempts = Number(job.attempts || 0) + 1;
      if (nextAttempts >= JOB_MAX_ATTEMPTS) {
        markJobFailed(job.id, nextAttempts);
      } else {
        markJobRetry(job.id, nextAttempts);
      }
    }
  }
}

