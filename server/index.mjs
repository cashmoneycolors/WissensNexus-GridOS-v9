// ML Training: tiny naive bayes with deterministic accuracy
function tokenize(text) {
  return text.toLowerCase().replace(/[^\p{L}\p{N}\s]+/gu, ' ').split(/\s+/).filter(Boolean);
}

function trainNB(samples) {
  const labels = [...new Set(samples.map((s) => s.label))];
  const labelCounts = Object.fromEntries(labels.map((l) => [l, 0]));
  const tokenCounts = Object.fromEntries(labels.map((l) => [l, {}]));
  const totalTokens = Object.fromEntries(labels.map((l) => [l, 0]));
  const vocab = new Set();

  for (const s of samples) {
    labelCounts[s.label] += 1;
    const toks = tokenize(s.text);
    for (const t of toks) {
      vocab.add(t);
      tokenCounts[s.label][t] = (tokenCounts[s.label][t] ?? 0) + 1;
      totalTokens[s.label] += 1;
    }
  }

  const vocabSize = Math.max(1, vocab.size);
  const totalSamples = samples.length;
  const priors = {};
  const likelihood = {};
  const unk = {};

  for (const l of labels) {
    priors[l] = Math.log((labelCounts[l] ?? 0) / totalSamples || 1e-12);
    const denom = totalTokens[l] + vocabSize;
    likelihood[l] = {};
    for (const t of vocab) {
      const c = tokenCounts[l][t] ?? 0;
      likelihood[l][t] = Math.log((c + 1) / denom);
    }
    unk[l] = Math.log(1 / denom);
  }

  return { labels, priors, likelihood, unk };
}

function predictNB(model, text) {
  const toks = tokenize(text);
  const scores = {};
  for (const l of model.labels) {
    let s = model.priors[l];
    for (const t of toks) s += model.likelihood[l][t] ?? model.unk[l];
    scores[l] = s;
  }
  let best = model.labels[0];
  for (const l of model.labels) if (scores[l] > scores[best]) best = l;
  return best;
}

app.post('/api/train', async () => {
  const labels = ['ops', 'market', 'infra'];
  const dataset = [];
  for (let i = 0; i < 120; i++) {
    dataset.push({ label: 'ops', text: `ops shard grid node uptime ${i}` });
    dataset.push({ label: 'market', text: `market trend revenue growth ${i}` });
    dataset.push({ label: 'infra', text: `infra region service latency ${i}` });
  }

  // split
  const test = dataset.filter((_, i) => i % 5 === 0);
  const train = dataset.filter((_, i) => i % 5 !== 0);

  const model = trainNB(train);
  let correct = 0;
  for (const s of test) if (predictNB(model, s.text) === s.label) correct += 1;
  const acc = correct / test.length;

  return {
    samples: dataset.length,
    test: test.length,
    accuracy: acc
  };
});

