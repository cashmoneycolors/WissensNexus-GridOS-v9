// Media analysis (text or file)
app.post('/api/media/analyze', async (req, reply) => {
  if (req.isMultipart()) {
    const data = await req.file();
    if (!data) return reply.code(400).send({ error: 'file required' });
    const buf = await data.toBuffer();
    const summary = `File ${data.filename} (${data.mimetype})`;
    const stats = { bytes: buf.length };
    const id = `media_${Date.now()}_${Math.random().toString(16).slice(2)}`;
    run('INSERT INTO media_analyses (id, kind, summary, stats, created_at) VALUES (?, ?, ?, ?, ?)', [
      id,
      'file',
      summary,
      JSON.stringify(stats),
      Date.now()
    ]);
    return { id, kind: 'file', summary, stats };
  }

  const { text } = req.body ?? {};
  if (!text) return reply.code(400).send({ error: 'text required' });
  const words = String(text).trim().split(/\s+/).filter(Boolean);
  const summary = `Text analysed: ${words.length} words`;
  const stats = { words: words.length, chars: String(text).length };
  const id = `media_${Date.now()}_${Math.random().toString(16).slice(2)}`;
  run('INSERT INTO media_analyses (id, kind, summary, stats, created_at) VALUES (?, ?, ?, ?, ?)', [
    id,
    'text',
    summary,
    JSON.stringify(stats),
    Date.now()
  ]);
  return { id, kind: 'text', summary, stats };
});

